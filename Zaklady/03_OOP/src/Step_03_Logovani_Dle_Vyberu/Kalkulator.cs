namespace JezekT.SkoleniCS.Kalkulator.ConsoleApp
{
    public class Kalkulator
    {
        public int GetSoucet(int x, int y)
        {
            return x + y;
        }

        public int GetRozdil(int x, int y)
        {
            return x - y;
        }

        public int GetSoucin(int x, int y)
        {
            return x * y;
        }

        public int GetPodil(int x, int y)
        {
            return x / y;
        }
    }
}
